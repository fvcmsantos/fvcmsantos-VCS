'''
Desafio > crie dois conjuntos e cada conjunto contendo 5 nomes de seus amigos. Alguns nomes devem estar presentes em ambos os conjuntos. Use o metodo para encontrar quais nomes aparecem em ambos os conjuntos e imprima o resultado.
'''

conjunto1 = {'João', 'Maria', 'José', 'Ana', 'Pedro'}

conjunto2 = {'Pedro', 'João', 'Bubble', 'Vanessa', 'Maria'}

print(conjunto1.intersection(conjunto2))

