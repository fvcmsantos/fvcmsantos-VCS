#agragando duas listas com o Zip
# Zip = juntar duas listas


cores = ['amarelo','verde', 'azul',	'vermelha' ]
valores = [10, 20, 30, 40]

duas_listas = zip(cores,valores)

print(list(duas_listas))

#ao acrescentar a função LIST ele tras as informações de suas listas)

#Essa função usa o index das listas para apresenta as informações, exemplos: amarelo é zero, logo o valor 10 = zero tambem.





