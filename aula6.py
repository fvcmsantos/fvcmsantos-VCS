# para escrever um texto usando variaveis, como por exemplo: "O Caio Victor Santos é um excelente programador"

nome = "Caio Victor"
Sobrenome = "Santos"
profissao = "Programador"  

texto = "o " + nome + " " + Sobrenome + " é um " + "excelente " + profissao

print(texto)
'///////'

"Outra opção para escrever usando menos variaveis e parametros"

texto2 = f'0 {nome} {Sobrenome} é um excelente {profissao}'

print(texto2)

# O f'', formata o texto de forma mais simples.








