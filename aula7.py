# formatar um texto, usando maiuscula e minuscula 

# lower = letras minusculas
# captalize = primeira letra maiuscula
# upper = letras maiusculas
# case sensitive = letras maiusculas e minusculas sao diferentes (C ou c)
# find = encontrar uma palavra específica dentro de um text  o
# raplace = substituir uma palavra específica dentro de um texto  
#Strip = tirar espaços em branco antes e depois da palavra


